import sys
sys.setrecursionlimit(500000)
INF = 10**17

def factorization(n):
    arr = []
    temp = n
    for i in range(2, int(-(-n**0.5//1))+1):
        if temp%i==0:
            cnt=0
            while temp%i==0:
                cnt+=1
                temp //= i
            arr.append([i, cnt])

    if temp!=1:
        arr.append([temp, 1])

    if arr==[]:
        arr.append([n, 1])

    return arr

K = II()

T = factorization(K)
ans = []
for n,c in T:
    D = {}
    cnt = 0
    now = 0
    while cnt < c:
        now += n
        h_now = now // n
        if h_now not in D:
            tmp = 1
        else:
            tmp = 1 + D[h_now]
        cnt += tmp
        D[now] = tmp
    ans.append(now)
print(max(ans))